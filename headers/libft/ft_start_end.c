void	ft_start_end(char *s, char c, int *pos)
{
	int i;

	i = 0;
	while (s[i] && s[i] == c)
		i++;
	if (i == 0)
	{
		*pos++ = 0;
		i++;
	}
	while (s[i])
	{
		if ((s[i] != c && s[i - 1] == c) ||
				(s[i] != c && (s[i + 1] == c || !s[i + 1])))
		{
			if ((s[i - 1] == s[i + 1] || !s[i + 1]) && s[i - 1] == c)
			{
				*pos = i;
				pos++;
			}
			*pos = i;
			pos++;
		}
		i++;
	}
}
